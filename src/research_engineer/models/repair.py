"""Phase 14 - Autonomous self-repair models.

Typed Pydantic models for the self-repair framework: structured failure
reports, repair strategies, repair cycle records, and repair configuration.

These models replace prompt-based error passing with structured failure
reports that flow through the repair pipeline, enabling systematic root-
cause analysis and strategy generation.
"""

from __future__ import annotations

from datetime import datetime
from enum import StrEnum
from typing import Any

from pydantic import BaseModel, Field

# ---------------------------------------------------------------------------
# Enums
# ---------------------------------------------------------------------------


class FailureCategory(StrEnum):
    """Category of failure detected during execution."""

    SYNTAX_ERROR = "syntax_error"
    IMPORT_ERROR = "import_error"
    ASSERTION_ERROR = "assertion_error"
    TYPE_ERROR = "type_error"
    ATTRIBUTE_ERROR = "attribute_error"
    RUNTIME_ERROR = "runtime_error"
    LOGIC_ERROR = "logic_error"
    MISSING_IMPLEMENTATION = "missing_implementation"
    REVIEW_REJECTION = "review_rejection"
    TEST_FAILURE = "test_failure"
    BUILD_FAILURE = "build_failure"
    TIMEOUT = "timeout"
    UNKNOWN = "unknown"


class FailureSeverity(StrEnum):
    """How severe a failure is."""

    CRITICAL = "critical"
    HIGH = "high"
    MEDIUM = "medium"
    LOW = "low"


class RepairActionType(StrEnum):
    """Kind of repair action a strategy proposes."""

    FIX_SYNTAX = "fix_syntax"
    FIX_IMPORT = "fix_import"
    FIX_TYPE = "fix_type"
    FIX_LOGIC = "fix_logic"
    ADD_MISSING_CODE = "add_missing_code"
    REFACTOR = "refactor"
    REGENERATE = "regenerate"
    ADJUST_CONFIG = "adjust_config"
    SKIP_TEST = "skip_test"
    MANUAL_REVIEW = "manual_review"


class RepairOutcome(StrEnum):
    """Outcome of a repair cycle."""

    PENDING = "pending"
    SUCCESS = "success"
    PARTIAL = "partial"
    FAILURE = "failure"
    SKIPPED = "skipped"
    STAGNATED = "stagnated"


class RepairTerminationReason(StrEnum):
    """Why the repair loop terminated."""

    SUCCESS = "success"
    BUDGET_EXHAUSTED = "budget_exhausted"
    NO_STRATEGIES = "no_strategies"
    STAGNATION = "stagnation"
    CRITICAL_FAILURE = "critical_failure"


# ---------------------------------------------------------------------------
# Failure report
# ---------------------------------------------------------------------------


class FailureReport(BaseModel):
    """Structured failure report produced by the FailureAnalyzer.

    Replaces ad-hoc error strings with a typed report containing the
    failure category, severity, root cause hypothesis, affected files,
    and actionable evidence. This is the primary input to the
    RepairStrategist.
    """

    report_id: str = Field(..., description="Unique report identifier")
    category: FailureCategory = Field(..., description="Failure category")
    severity: FailureSeverity = Field(
        default=FailureSeverity.MEDIUM, description="How severe"
    )
    root_cause: str = Field(
        default="", description="Hypothesized root cause"
    )
    evidence: list[str] = Field(
        default_factory=list,
        description="Supporting evidence (error messages, tracebacks)",
    )
    affected_files: list[str] = Field(
        default_factory=list,
        description="Files likely causing or affected by the failure",
    )
    affected_symbols: list[str] = Field(
        default_factory=list,
        description="Symbols (functions/classes) involved",
    )
    failure_source: str = Field(
        default="",
        description="Source: 'review', 'test', 'implementation', 'build'",
    )
    raw_error: str = Field(
        default="", description="Original error string"
    )
    timestamp: datetime = Field(default_factory=datetime.now)
    metadata: dict[str, Any] = Field(
        default_factory=dict, description="Extra context"
    )


# ---------------------------------------------------------------------------
# Repair strategy
# ---------------------------------------------------------------------------


class RepairStrategy(BaseModel):
    """A single repair strategy generated by the RepairStrategist.

    Describes what to fix, how to fix it, and which files/symbols are
    involved. The framework selects the best strategy (by priority) and
    applies it.
    """

    strategy_id: str = Field(..., description="Unique strategy identifier")
    action_type: RepairActionType = Field(
        ..., description="Kind of repair action"
    )
    description: str = Field(
        ..., description="Human-readable description of the repair"
    )
    target_files: list[str] = Field(
        default_factory=list,
        description="Files to modify",
    )
    target_symbols: list[str] = Field(
        default_factory=list,
        description="Symbols to modify",
    )
    instructions: str = Field(
        default="",
        description="Detailed instructions for the coding agent",
    )
    priority: int = Field(
        default=5,
        ge=1,
        le=10,
        description="Priority (10=highest). Higher = applied first.",
    )
    confidence: float = Field(
        default=0.5,
        ge=0.0,
        le=1.0,
        description="Confidence this strategy will fix the failure",
    )
    estimated_effort: str = Field(
        default="low",
        description="low, medium, high",
    )


# ---------------------------------------------------------------------------
# Repair cycle record
# ---------------------------------------------------------------------------


class RepairCycle(BaseModel):
    """Record of a single repair cycle (analyze → strategize → apply → validate).

    Each iteration of the repair loop produces one :class:`RepairCycle`
    for traceability and convergence detection.
    """

    cycle_number: int = Field(..., description="0-indexed cycle number")
    failure_report: FailureReport | None = Field(
        default=None, description="Failure analyzed in this cycle"
    )
    strategies: list[RepairStrategy] = Field(
        default_factory=list,
        description="Strategies generated for this cycle",
    )
    applied_strategy: RepairStrategy | None = Field(
        default=None, description="Strategy that was applied"
    )
    outcome: RepairOutcome = Field(
        default=RepairOutcome.PENDING, description="Cycle outcome"
    )
    review_passed: bool = Field(
        default=False, description="Whether review passed after repair"
    )
    test_passed: bool = Field(
        default=False, description="Whether tests passed after repair"
    )
    duration_seconds: float = Field(default=0.0)
    notes: str = Field(default="", description="Additional notes")
    timestamp: datetime = Field(default_factory=datetime.now)


# ---------------------------------------------------------------------------
# Repair config
# ---------------------------------------------------------------------------


class RepairConfig(BaseModel):
    """Configuration for the self-repair loop.

    Controls retry budgets, termination conditions, and convergence
    detection thresholds.
    """

    max_iterations: int = Field(
        default=3,
        ge=0,
        le=20,
        description="Max repair cycles before giving up",
    )
    stagnation_threshold: int = Field(
        default=3,
        ge=1,
        description="Consecutive identical failures before declaring stagnation",
    )
    require_review: bool = Field(
        default=True,
        description="Run code review after each repair",
    )
    require_tests: bool = Field(
        default=True,
        description="Run tests after each repair",
    )
    min_confidence_threshold: float = Field(
        default=0.2,
        ge=0.0,
        le=1.0,
        description="Skip strategies below this confidence",
    )
    max_strategies_per_cycle: int = Field(
        default=5,
        ge=1,
        le=20,
        description="Max strategies to consider per cycle",
    )


# ---------------------------------------------------------------------------
# Repair result (summary)
# ---------------------------------------------------------------------------


class RepairResult(BaseModel):
    """Final summary of the self-repair process.

    Returned by :meth:`SelfRepairFramework.run` and embedded in the
    :class:`~research_engineer.models.task.TaskResult` for traceability.
    """

    total_cycles: int = Field(
        default=0, description="Total repair cycles executed"
    )
    successful: bool = Field(
        default=False, description="Whether repair ultimately succeeded"
    )
    termination_reason: RepairTerminationReason = Field(
        default=RepairTerminationReason.BUDGET_EXHAUSTED,
        description="Why the loop terminated",
    )
    cycles: list[RepairCycle] = Field(
        default_factory=list, description="All repair cycle records"
    )
    final_failure_report: FailureReport | None = Field(
        default=None, description="Last failure report if unsuccessful"
    )
    total_duration_seconds: float = Field(default=0.0)
    strategies_tried: int = Field(
        default=0, description="Total strategies applied across all cycles"
    )
    unique_failures: int = Field(
        default=0, description="Number of distinct failure categories seen"
    )


